let x = 5;
x += 3;  // x = x + 3 => x = 5 + 3 = 8
x *= 2;  // x = x * 2 => x = 8 * 2 = 16
x -= 4;  // x = x - 4 => x = 16 - 4 = 12
x /= 3;  // x = x / 3 => x = 12 / 3 = 4

console.log(4); // Выведем итоговый результат в консоль

